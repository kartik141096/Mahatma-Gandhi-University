            <h3 class="mt-3">Eligibility for Admission for M. PHIL & PH. D Programme</h3>
            <p>
                <strong>1.</strong> A candidate must have obtained Master's degree or equivalent in related subject area with at least 55% marks (or its equivalent grade 'B' in the UGC 7-point scale); 
                and second division of the degree level examination from any University/Institute in India or abroad, which is established by law and empowered to grant degrees.
            </p>
            <p>
                <strong>2.</strong> A relaxation of 5% of marks, or an equivalent relaxation of grade, may be allowed for the SC/ST/OBC (non-creamy layer)/differently-able and other categories of 
                candidates. However, such candidates must have secured at least second class in degree level examination.
            </p>
        </div>
        
        <div class="mt-3">
            <h3 class="mt-3">Duration of the Programme</h3>
            <p>
                <strong>1.</strong> M.Phil. programme shall be for a minimum duration of two (2) consecutive semesters/one year and a maximum of four (4) consecutive semesters/two years.
            </p>
            <p>
                <strong>2.</strong> Ph.D. programme shall be for a minimum duration of three years, including course work and a maximum of six years.
            </p>
            <p>
                <strong>3.</strong> Extension beyond the above limits will be governed by the relevant clauses as stipulated in the Statute/Ordinance of the individual institution concerned.
            </p>
