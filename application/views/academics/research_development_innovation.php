                in regular "fulltime" mode. The programme focuses on different specialized subjects, as specified and amended by the Research Degree Committee (RDC) 
                of the Research, Development and Innovation Cell (RDIC).
            </p>
            <p>
                In order to enhance the stakeholder engagement in the doctoral research process, the university has partnered with international agencies like United 
                Nations Global Compact Network India (GCNI) and Inter-University Sustainable Development Research Partnership (IUSDRP). Through these partnerships, 
                the researchers can explore national and international engagement opportunities, available from time to time. Research at MGU focuses on the development 
                of analytical skills in making critical judgments, as required for effective analysis of theories and practices in different knowledge areas with emphasis 
                on sustainable development issues. Moreover, the programme also aims at contributing the knowledge development and execution on different subject areas 
                related to the North-eastern region of India under the centre of Northeast Studies. The MGU community is engaged in creating new knowledge by following a 
                value system. Thus, MGU, through its "Centre for Value Studies" seeks to explore how values and ethics can be institutionalized within rapid developmental 
                transformations.
            </p>
